package com.edureka.spring.helloworld;

public class Employee {
	
	private long empId;
	
	private String name;

	public long getEmpId() {
		return empId;
	}

	public void setEmpId(long empId) {
		this.empId = empId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
